package jcms.integrationtier.dto;

/**
 *
 * @author mkamato
 */
public class HistogramUnitDTO {
    
    private Integer volume;
    //if filtering by strain example would be DBA\2J
    private String filterUnit;

    /**
     * @return the volume
     */
    public Integer getVolume() {
        return volume;
    }

    /**
     * @param volume the volume to set
     */
    public void setVolume(Integer volume) {
        this.volume = volume;
    }

    /**
     * @return the filterUnit
     */
    public String getFilterUnit() {
        return filterUnit;
    }

    /**
     * @param filterUnit the filterUnit to set
     */
    public void setFilterUnit(String filterUnit) {
        this.filterUnit = filterUnit;
    }
    
}
